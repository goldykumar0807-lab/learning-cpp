//pre and post increment and decrement operator.
#include <iostream>
using namespace std;
int main() {
    int x = 5;
    cout<<x++<<endl; //post increment
    cout<<++x<<endl; //pre increment
    cout<<x--<<endl; //post decrement
    cout<<--x<<endl; //pre decrement
    cout<<x<<endl;
    return 0;
}
